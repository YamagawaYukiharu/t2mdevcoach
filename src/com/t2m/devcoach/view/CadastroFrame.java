    private void btncadastrarclienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncadastrarclienteActionPerformed

            
        
        Pessoa pessoa = new Pessoa();

        if ((!txtnomecliente.getText().equals("")) 
                && (!txtdocumentocliente.getText().equals(""))
                && (!txtemail.getText().equals(""))) {
            {
               
                pessoa.setDatanasc(txtdatanasc.getText());
                pessoa.setDocumento(txtdocumentocliente.getText());
                pessoa.setNome(txtnomecliente.getText());
                pessoa.setEmail(txtemail.getText());

                try {
                    EntityManagerFactory objFactory = Persistence.createEntityManagerFactory("t2mPU");
                    PessoaJpaController jpa = new PessoaJpaController(objFactory);

                    jpa.create(pessoa);
                } catch (Exception ex) {
                    Logger.getLogger(CadastroFrame.class.getName()).log(Level.SEVERE, null, ex);
                } 
            }}    
        else JOptionPane.showMessageDialog(null, "Necessario inserir nome, documento e email!", "Erro", JOptionPane.ERROR_MESSAGE);
            
      
        
        
    }//GEN-LAST:event_btncadastrarclienteActionPerformed

